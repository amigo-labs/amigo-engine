/// Returns the glyph bitmap for an ASCII char, or None for unprintable.
pub fn glyph(ch: u8) -> Option<&'static [u8; 7]> {
    if ch >= FIRST_CHAR && ch <= LAST_CHAR {
        Some(&FONT_DATA[(ch - FIRST_CHAR) as usize])
    } else {
        None
    }
}

/// Measure the width of a string in pixels (including spacing).
pub fn measure_text(text: &str) -> (f32, f32) {
    if text.is_empty() {
        return (0.0, 0.0);
    }
    let chars = text.len() as f32;
    let w = chars * GLYPH_W as f32 + (chars - 1.0).max(0.0) * GLYPH_SPACING as f32;
    (w, GLYPH_H as f32)
}
