def bitmap_to_contours(rows):
    """Convert 5x7 bitmap to list of rectangular contours (font units, Y-up)."""
    contours = []
    for row_idx, row_byte in enumerate(rows):
        for col in range(5):
            if row_byte & (0x80 >> col):
                x0 = col * PIXEL
                y0 = (GLYPH_H - 1 - row_idx) * PIXEL
                x1 = x0 + PIXEL
                y1 = y0 + PIXEL
                contours.append([(x0, y0), (x1, y0), (x1, y1), (x0, y1)])
    return contours


def main():
    glyph_names = [".notdef"]
    char_map = {}

    for code in range(32, 127):
        name = "space" if code == 32 else f"uni{code:04X}"
        glyph_names.append(name)
        char_map[code] = name

    advance_width = (GLYPH_W + 1) * PIXEL  # 600

    fb = FontBuilder(EM, isTTF=True)
    fb.setupGlyphOrder(glyph_names)
    fb.setupCharacterMap(char_map)

    # Draw glyphs using pen
    drawing = {}
    for i, code in enumerate(range(32, 127)):
        name = "space" if code == 32 else f"uni{code:04X}"
        pen = TTGlyphPen(None)
        contours = bitmap_to_contours(FONT_DATA[i])
        for contour in contours:
            pen.moveTo(contour[0])
            for pt in contour[1:]:
                pen.lineTo(pt)
            pen.closePath()
        drawing[name] = pen.glyph()

    # .notdef: empty box
    pen = TTGlyphPen(None)
    drawing[".notdef"] = pen.glyph()

    fb.setupGlyf(drawing)

    # Metrics
    metrics = {}
    for name in glyph_names:
        metrics[name] = (advance_width, 0)
    fb.setupHorizontalMetrics(metrics)

    fb.setupHorizontalHeader(ascent=EM, descent=0)

    fb.setupNameTable({
        "familyName": "AmigoPixel",
        "styleName": "Regular",
    })

    fb.setupOS2(
        sTypoAscender=EM,
        sTypoDescender=0,
        sTypoLineGap=PIXEL,
        usWinAscent=EM,
        usWinDescent=0,
        sxHeight=GLYPH_H * PIXEL // 2,
        sCapHeight=GLYPH_H * PIXEL,
    )

    fb.setupPost()

    out_path = "crates/amigo_render/src/amigo_pixel.ttf"
    fb.font.save(out_path)

    import os
    size = os.path.getsize(out_path)
    print(f"Generated {out_path} ({len(glyph_names)} glyphs, {size} bytes)")


if __name__ == "__main__":
    main()
